package com;

public class MK {
    private String kodeMk;
    private String namaMk;

    public MK(String kodeMk, String namaMk) {
        this.kodeMk = kodeMk;
        this.namaMk = namaMk;
    }

    public String getKodeMk() {
        return this.kodeMk;
    }

    public void setKodeMk(String kodeMk) {
        this.kodeMk = kodeMk;
    }

    public String getNamaMk() {
        return this.namaMk;
    }

    public void setNamaMk(String namaMk) {
        this.namaMk = namaMk;
    }

}
